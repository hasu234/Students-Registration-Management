    private void adminSubmitBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminSubmitBTNActionPerformed
        // TODO add your handling code here:

        try{

            stmt = conn.createStatement();
            
            int adId = Integer.parseInt(adminID.getText());

            String adName = adminName.getText();
            String adMail = adminEmail.getText();
            String adPass = adminPassword.getText();

            String sql = "UPDATE admin SET name = '"+adName+"', mail = '"+adMail+"', password= '"+adPass+"' WHERE id = '"+adId+"'";

            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Data Updated!");

        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_adminSubmitBTNActionPerformed

