    private void adminSearchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminSearchBTNActionPerformed
        // TODO add your handling code here:
        
        try{
            stmt = conn.createStatement();
            int adId = Integer.parseInt(adminID.getText());
            
            String sql = "SELECT * FROM admin WHERE id = '"+adId+"'";
            rs = stmt.executeQuery(sql);
            
            if (rs.next()) {
                
                adminName.setText(rs.getString("name"));
                adminEmail.setText(rs.getString("mail"));
                adminPassword.setText(rs.getString("password"));
                
                
            } else {
                
               JOptionPane.showMessageDialog(null, "Record Not Found!");
            }
            
            
            
        }catch(NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_adminSearchBTNActionPerformed

